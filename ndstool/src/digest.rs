use std::io::{Read, Seek};

use hmac::{Hmac, KeyInit, Mac};
use sha1::Sha1;

use crate::Result;

const HMAC_SHA1_KEY: [u8; 0x40] = [
    0x21, 0x06, 0xC0, 0xDE, 0xBA, 0x98, 0xCE, 0x3F, 0xA6, 0x92, 0xE3, 0x9D, 0x46, 0xF2, 0xED, 0x01,
    0x76, 0xE3, 0xCC, 0x08, 0x56, 0x23, 0x63, 0xFA, 0xCA, 0xD4, 0xEC, 0xDF, 0x9A, 0x62, 0x78, 0x34,
    0x8F, 0x6D, 0x63, 0x3C, 0xFE, 0x22, 0xCA, 0x92, 0x20, 0x88, 0x97, 0x23, 0xD2, 0xCF, 0xAE, 0xC2,
    0x32, 0x67, 0x8D, 0xFE, 0xCA, 0x83, 0x64, 0x98, 0xAC, 0xFD, 0x3E, 0x37, 0x87, 0x46, 0x58, 0x24,
];

pub fn sha1_hmac(reader: &mut (impl Read + Seek), position: u32, size: u32) -> Result<[u8; 20]> {
    let mut hasher =
        Hmac::<Sha1>::new_from_slice(&HMAC_SHA1_KEY).expect("HMAC can take key of any size");
    let mut read_buffer = [0u8; 0x1000];
    reader.seek(std::io::SeekFrom::Start(position as u64))?;
    let mut truncated_reader = reader.take(size as u64);
    let mut remaining_size = size as usize;
    while remaining_size > 0 {
        let read_bytes = truncated_reader.read(&mut read_buffer)?;
        hasher.update(&read_buffer[..read_bytes]);
        remaining_size -= read_bytes;
    }
    let result = hasher.finalize();
    let hash = result.into_bytes();

    Ok(hash.into())
}
